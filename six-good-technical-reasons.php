    <div class="article_text">
      <h2 class="title_09">3.	Expandable</h2>
      <p>
	From the smallest brick to the complete core itself, everything is presented as a “bundle” (or plug-in in Symfony language) in Symfony 2.0.  Each bundle is intended to add functionality to the framework, of course, and each bundle can also be reused in another project or shared with the rest of the community.
	<br>
	In any case, the system of bundles allows everything to change within Symfony, including the core itself. Using the system’s interface contracts between bricks, the behavior of the framework thus can be changed at will, without requiring complete reconfiguration.
      </p>
    </div>

  </div>

