      <h2 class="title_09">4.	Stable and sustainable</h2>
      <p>
	Developed by Sensio Labs, major versions of Symfony are all supported for 3 years by the company.  And even for life as far as security-related issues are concerned.
	<br>
	For even greater stability, the minor versions of Symfony 2.0’s contract and interface are also guaranteed and compatibility between all minor versions will be ensured on the API defined by the public interfaces.
      </p>
    </div>

