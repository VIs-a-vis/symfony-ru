      <h2 class="title_09">5.	The joy of developing</h2>
      <p>
	As a highly functional environment, Symfony 2.0 also guarantees a certain comfort level for developers.  By taking care of a number of unpleasant tasks (development of minor functionalities, for example), Symfony 2.0 allows developers to focus on the actual highlights of an application and to both fully validate their role and improve their productivity.
	<br>
	Among Symfony’s tools designed to make the life of a developer much easier, there is the legendary Web Debug Toolbar, as well as native support for development environments, detailed error pages or even native security.
      </p>
